		'post_status'    => 'publish',
		'posts_per_page' => -1,
	)
);
$city_ids = array();

foreach ( $cities as $city ) {
	$city_ids[ mb_strtolower( $city->post_title ) ] = $city->ID;
}

$created = 0;
$updated = 0;
$skipped = 0;

foreach ( $branches as $data ) {
	$city_id = $city_ids[ mb_strtolower( $data['city'] ) ] ?? 0;

	if ( ! $city_id ) {
		++$skipped;
		fwrite( STDERR, "Місто не знайдено: {$data['city']}\n" );
		continue;
	}

	$matches = get_posts(
		array(
			'post_type'      => 'branch',
			'post_status'    => 'any',
			'posts_per_page' => 1,
			'meta_key'       => 'branch_external_id',
			'meta_value'     => $data['external_id'],
		)
	);
	$branch = current( $matches );
	$branch_id = $branch instanceof WP_Post ? $branch->ID : wp_insert_post(
		array(
			'post_type'   => 'branch',
			'post_title'  => 'Logika — ' . $data['city'],
			'post_status' => 'publish',
		),
		true
	);

	if ( is_wp_error( $branch_id ) ) {
		++$skipped;
		fwrite( STDERR, "Не вдалося зберегти філію: {$data['city']}\n" );
		continue;
	}

	update_field( 'branch_external_id', $data['external_id'], $branch_id );
	update_field( 'branch_city_id', $city_id, $branch_id );
	update_field( 'branch_address', $data['address'], $branch_id );
	update_field( 'branch_address_hash', hash( 'sha256', mb_strtolower( $data['address'] ) ), $branch_id );
	update_field( 'branch_is_active', 1, $branch_id );

	if ( $branch instanceof WP_Post ) {
		++$updated;
	} else {
		++$created;
	}
}

echo "Філії Tilda: створено {$created}, оновлено {$updated}, пропущено {$skipped}.\n";
